//A simple program that shows how to use integer variables to make simple calculations
#include <iostream>
using namespace std;
int main(void){
    int numA, numB, sum;
    numA=10;
    numB=15;
    cout << "Value of numA:";
    cout << numA << "\n";
    cout << "Value of numB:";
    cout << numB << "\n";
    sum=numA+numB;
    cout << "Value of sum:";
    cout << sum << "\n";
}
